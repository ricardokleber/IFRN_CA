'''
PEOO - #00 - Lista de Exercícios de Fixação
Questão 02 - Escreva um script Python que imprima os números de 1 a 9
separados por vírgula em uma única linha

Forma de Responder 004: Utilizando WHILE e PRINT
'''

numero = 1

while numero < 9:
    print(numero,end=',')
    numero += 1

print(numero)

