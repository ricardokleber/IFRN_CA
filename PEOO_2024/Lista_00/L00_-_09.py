'''
PEOO - #00 - Lista de Exercícios de Fixação
Questão 09 - Escreva um script Python que recebe o valor
da base e da altura de um triângulo e informa o valor de
sua área. Dica: A área de um triângulo é igual à
multiplicação de sua base x altura / 2.
'''
base = float(input("Informe o valor da BASE do Triângulo: "))
altura = float(input("Informe o valor da ALTURA do Triângulo: "))
print("A Área do Triângulo é", base * altura / 2)